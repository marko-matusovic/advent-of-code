    tiles = set([])
    for move in moves:
        x=y=z = 0
        for m in move:
            if m == 'w':
                x -= 1
                y += 1
            elif m == 'e':
                x += 1
                y -= 1
            elif m == 'sw':
                x -= 1
                z += 1
            elif m == 'se':
                y -= 1
                z += 1
            elif m == 'nw':
                y += 1
                z -= 1
            elif m == 'ne':
                x += 1
                z -= 1
            else:
                print('Error, unknown direction',m)
                return
        tile = hash(x,y,z)
        if tile in tiles:
            tiles.remove(tile)
        else:
            tiles.add(tile)
    print('Part 1:', len(tiles))
    return tiles

